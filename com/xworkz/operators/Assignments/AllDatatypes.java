class AllDatatypes{
public static void main(String args[]){
boolean flag=true;
System.out.println("The Boolean flag value is : " + flag);
char letter = 'D';
System.out.println("The char value is : " + letter);
Float f=12.123f;
System.out.println("The float value is : " + f);
double d=441.22222;
System.out.println("The double number value is : " + d);
long l=-12222222356596L;
System.out.println("The long number value is : " + l);
short s=-32768;
System.out.println("The short number value is : " + s);
int num=111;
System.out.println("The integer value is : " + num);
byte byt=108;
System.out.println("The byte value is : " + byt);


}}
