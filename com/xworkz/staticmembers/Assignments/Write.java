package com.xworkz.staticmembers.Assignments;

public class Write {
	
	public void oneparamwrite(String name) {
		System.out.println("Name :" + name);
	}
	public void twoparamwrite(String firstname,String lastname) {
		System.out.println("first Name :" + firstname);
		System.out.println("Last Name :" + lastname);
	}
	public void twoparamCSwrite(char a,String name) {
		System.out.println("Initial :" + a);
		System.out.println("Name :" + name);
	}
	public void twoparamCIwrite(char n,int rollnum) {
		System.out.println("Initial :" + n);
		System.out.println("Roll number :" + rollnum);
	}
	
	
}
