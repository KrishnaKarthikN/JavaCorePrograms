package com.xworkz.LoopingConcept;

public class WhileLoopEx {
	public static void main(String[] args) {
		System.out.println("Ascending");
		int i = 1;
		while(i<=10) {
			System.out.print(i);
			i++;
		}
		System.out.println();
		
		System.out.println("Decending");
		i = 10;
		while(i>=1) {
			System.out.print(i);
			i--;
		}

	
	
	}
}
