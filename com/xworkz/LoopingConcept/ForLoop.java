package com.xworkz.LoopingConcept;

public class ForLoop {
	
	public static void main(String[] args) {
		System.out.println("Prog to illustrate for loop");
		
		
		for (int i = 1; i <= 3; i++) {
			System.out.println("Hello!!!");
		}
				
		
		
		System.out.println("Prog to illustrate for loop");
	}
	
}
 